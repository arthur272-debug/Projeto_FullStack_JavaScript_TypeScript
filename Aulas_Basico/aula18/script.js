// Revisando os operadores de comparação

console.log(10 == 10); // true
console.log(10 == 5); // false
console.log(5 == '5'); // true

console.log(10 === 10); // true
console.log(10 === 5); // false

console.log(10 != 10); // false
console.log(10 != 5); // true

console.log(10 !== 10); // false
console.log(10 !== 5); // true

console.log(10 > 5); // true
console.log(10 > 10); // false

console.log(10 >= 10); // true
console.log(5 >= 20); // false

console.log(10 < 20); // true
console.log(10 < 10); // false

console.log(10 <= 10); // true
console.log(10 <= 5); // false